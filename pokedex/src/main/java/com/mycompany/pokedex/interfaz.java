    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:
        String name = nombre.getText();
        String type = nombre.getText();
        String hp = PS.getText();
        String at = ataque.getText();
        String df = defensa.getText();
        String atE = ataqueE.getText();
        String dfE = defensaE.getText();
        String speed = velocidad.getText();
        
        Pokemon[id][0] = name;
        Pokemon[id][1] = type;
        Pokemon[id][2] = hp;
        Pokemon[id][3] = at;
        Pokemon[id][4] = df;
        Pokemon[id][5] = atE;
        Pokemon[id][6] = dfE;
        Pokemon[id][7] = speed;
        
        id++;
        
        nombre.setText("");
        tipo.setText("");
        PS.setText("");
        ataque.setText("");
        defensa.setText("");
        ataqueE.setText("");
        defensaE.setText("");
        velocidad.setText("");
        
        JOptionPane.showMessageDialog(null, "Se guardo kp.\n");
        
    }//GEN-LAST:event_btn_guardarActionPerformed

    private void btn_mostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_mostrarActionPerformed
        // TODO add your handling code here:
        registro.setText("");
        StringBuilder datosPokemon = new StringBuilder("Datos: \n");
            if(id > 0){
            for(int i = 0; i < id; ++i){
            datosPokemon.append("Nombre: ").append(Pokemon[i][0]).append("\n")
            .append("Tipo: ").append(Pokemon[i][1]).append("\n")  
            .append("PS: ").append(Pokemon[i][2]).append("\n")         
            .append("Ataque: ").append(Pokemon[i][3]).append("\n")
            .append("Defensa: ").append(Pokemon[i][4]).append("\n")
            .append("Ataque Especial: ").append(Pokemon[i][5]).append("\n")
            .append("Defensa Especial: ").append(Pokemon[i][6]).append("\n")
            .append("Velocidad: ").append(Pokemon[i][7]).append("\n");
            
        }
            registro.setText(datosPokemon.toString());
            } else{
        JOptionPane.showMessageDialog(null, "no se encontroraron CLIENTES");
    }
    }//GEN-LAST:event_btn_mostrarActionPerformed

    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        // TODO add your handling code here:
                String search = buscar.getText();
        
        boolean encontrado = false;
        for(int i = 0; i < id; i++){
            if(Pokemon[i][0].equalsIgnoreCase(search)){
                for(int j = i; j < id; j++){
                    Pokemon[j] = Pokemon[j + 1];
                }
                id--;
        nombre.setText("");
        tipo.setText("");
        PS.setText("");
        ataque.setText("");
        defensa.setText("");
        ataqueE.setText("");
        defensaE.setText("");
        velocidad.setText("");
        registro.setText("");
                JOptionPane.showMessageDialog(null, "Se elimino del registro");
                
                encontrado = true;
            }
        }
        if(!encontrado){
            JOptionPane.showMessageDialog(null, "El registro no existe...");            
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

