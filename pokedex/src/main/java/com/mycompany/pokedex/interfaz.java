    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        // TODO add your handling code here:
  String search = buscar.getText();
boolean encontrado = false;

for(int i = 0; i < id; i++){
    if(Pokemon[i][0].equalsIgnoreCase(search)){
        nombre.setText(Pokemon[i][0]);
        tipo.setText(Pokemon[i][1]);
        PS.setText(Pokemon[i][2]);
        ataque.setText(Pokemon[i][3]);
        defensa.setText(Pokemon[i][4]);
        ataqueE.setText(Pokemon[i][5]);
        defensaE.setText(Pokemon[i][6]);
        velocidad.setText(Pokemon[i][7]);
        encontrado = true;
        break;  // Salir del bucle una vez encontrado el PokÃ©mon
    }
}

if(!encontrado){
    JOptionPane.showMessageDialog(null, "No se encontraron registros");
    nombre.setText("");
    tipo.setText("");
    PS.setText("");
    ataque.setText("");
    defensa.setText("");
    ataqueE.setText("");
    defensaE.setText("");
    velocidad.setText("");
}

    }//GEN-LAST:event_btn_buscarActionPerformed

